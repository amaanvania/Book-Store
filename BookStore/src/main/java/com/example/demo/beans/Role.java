package com.example.demo.beans;


/*
    Bean which stores Role object
    Used to assign roles
*/
public class Role {

    //primary key
    int id;

    String name;

    public Role(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
